"""
Authoring-density baseline: multi-source order processing pipeline.
LLM-GENERATED STYLE — verbose, defensive, fully annotated.

Semantically equivalent to examples/workflows/data_pipeline.ainl.

This file models what a capable LLM (GPT-4o / Claude Sonnet) produces when
asked to implement the same pipeline from scratch in Python — complete with:
  - Full type annotations and docstrings
  - Logging setup
  - Retry wrappers for external calls
  - Defensive input validation
  - Dataclasses for all structured types
  - File-backed cache with proper error handling
  - Memory/audit log append helper
  - Per-function try/except with structured error returns
  - Argparse CLI entry point

This is the correct comparison baseline for the "3–5× authoring density"
claim: the README states savings vs Python/TypeScript "generated by an LLM,"
not vs terse hand-optimised code.

Dependencies: httpx, openai, tenacity  (pip install httpx openai tenacity)
"""

from __future__ import annotations

import argparse
import json
import logging
import time
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from typing import Optional, Any

import httpx
from openai import OpenAI
from tenacity import retry, stop_after_attempt, wait_exponential

# ---------------------------------------------------------------------------
# Logging
# ---------------------------------------------------------------------------

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s %(levelname)-8s %(name)s: %(message)s",
)
logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Enumerations
# ---------------------------------------------------------------------------

class FulfilmentType(str, Enum):
    DIGITAL      = "digital"
    PHYSICAL     = "physical"
    SUBSCRIPTION = "subscription"


class OrderStatus(str, Enum):
    PROCESSING_DIGITAL       = "processing:digital"
    PROCESSING_PHYSICAL      = "processing:physical"
    PROCESSING_SUBSCRIPTION  = "processing:subscription"
    PROCESSING_VIP           = "processing:vip"
    PROCESSING_ENTERPRISE_VIP = "processing:enterprise_vip"
    BACKORDER_PHYSICAL       = "backorder:physical"
    REJECTED_FRAUD           = "rejected:fraud"
    REJECTED_INVALID         = "rejected:invalid"
    DUPLICATE                = "duplicate"


# ---------------------------------------------------------------------------
# Dataclasses
# ---------------------------------------------------------------------------

@dataclass
class CustomerData:
    """Enriched customer record returned by the customer API."""
    customer_id: str
    tier: str
    fraud_score: int
    discount_pct: float
    subscription_id: Optional[str] = None


@dataclass
class ProductData:
    """Product record returned by the product/inventory API."""
    product_id: str
    name: str
    inventory: int
    category: str


@dataclass
class OrderInput:
    """Validated and parsed order event."""
    order_id: str
    customer_id: str
    product_id: str
    order_value: float
    fulfilment_type: FulfilmentType
    customer_api: str
    product_api: str


@dataclass
class OrderResult:
    """Pipeline execution result."""
    order_id: str
    status: OrderStatus
    dispatch_record: str
    confirmation_email: Optional[str] = None
    from_cache: bool = False
    processing_time_ms: float = 0.0
    error: Optional[str] = None


# ---------------------------------------------------------------------------
# File-backed cache (mirrors AINL cache adapter)
# ---------------------------------------------------------------------------

class FileCache:
    """Persistent JSON key-value store backed by a local file."""

    def __init__(self, path: str = ".order_cache.json") -> None:
        self._path = Path(path)
        self._data: dict[str, str] = {}
        if self._path.exists():
            try:
                self._data = json.loads(self._path.read_text(encoding="utf-8"))
            except (json.JSONDecodeError, OSError) as exc:
                logger.warning("Cache file corrupt, starting fresh: %s", exc)
                self._data = {}

    def get(self, key: str) -> Optional[str]:
        return self._data.get(key)

    def set(self, key: str, value: str) -> None:
        self._data[key] = value
        try:
            self._path.write_text(
                json.dumps(self._data, indent=2), encoding="utf-8"
            )
        except OSError as exc:
            logger.error("Failed to persist cache entry '%s': %s", key, exc)


# ---------------------------------------------------------------------------
# Append-only memory log (mirrors AINL memory adapter APPEND)
# ---------------------------------------------------------------------------

class MemoryLog:
    """Append-only JSONL audit log for VIP order confirmations."""

    def __init__(self, path: str = ".order_memory.jsonl") -> None:
        self._path = Path(path)

    def append(
        self,
        namespace: str,
        tag: str,
        order_id: str,
        payload: str,
    ) -> None:
        entry = {
            "namespace": namespace,
            "tag": tag,
            "order_id": order_id,
            "payload": payload,
            "ts": time.time(),
        }
        try:
            with self._path.open("a", encoding="utf-8") as fh:
                fh.write(json.dumps(entry) + "\n")
        except OSError as exc:
            logger.error("Failed to write memory log: %s", exc)


# ---------------------------------------------------------------------------
# HTTP helpers with retry
# ---------------------------------------------------------------------------

@retry(stop=stop_after_attempt(3), wait=wait_exponential(multiplier=1, min=1, max=4))
def _fetch_json(url: str, timeout: float = 10.0) -> dict[str, Any]:
    """Fetch a JSON resource with exponential-backoff retry."""
    logger.debug("HTTP GET %s", url)
    with httpx.Client(timeout=timeout) as client:
        response = client.get(url)
        response.raise_for_status()
        return response.json()


def fetch_customer(api_base: str, customer_id: str) -> CustomerData:
    """Fetch and parse customer enrichment data."""
    data = _fetch_json(f"{api_base}{customer_id}")
    return CustomerData(
        customer_id=customer_id,
        tier=data.get("tier", "standard"),
        fraud_score=int(data.get("fraud_score", 0)),
        discount_pct=float(data.get("discount_pct", 0.0)),
        subscription_id=data.get("subscription_id"),
    )


def fetch_product(api_base: str, product_id: str) -> ProductData:
    """Fetch and parse product and inventory data."""
    data = _fetch_json(f"{api_base}{product_id}")
    return ProductData(
        product_id=product_id,
        name=data.get("name", ""),
        inventory=int(data.get("inventory", 0)),
        category=data.get("category", ""),
    )


# ---------------------------------------------------------------------------
# LLM helpers
# ---------------------------------------------------------------------------

@retry(stop=stop_after_attempt(3), wait=wait_exponential(multiplier=1, min=1, max=8))
def _llm_complete(
    prompt: str,
    max_tokens: int,
    temperature: float,
    openai_client: OpenAI,
) -> str:
    """Call LLM with retry on transient failures."""
    completion = openai_client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}],
        max_tokens=max_tokens,
        temperature=temperature,
    )
    return completion.choices[0].message.content or ""


def generate_enterprise_confirmation(
    customer_id: str,
    product_name: str,
    openai_client: OpenAI,
) -> str:
    """Generate a personalised enterprise order confirmation email."""
    prompt = (
        f"Generate a professional enterprise order confirmation for {customer_id} "
        f"ordering {product_name}. "
        "Mention dedicated account manager, priority SLA, and invoice terms."
    )
    return _llm_complete(prompt, max_tokens=250, temperature=0.4, openai_client=openai_client)


def generate_vip_confirmation(
    customer_id: str,
    product_name: str,
    openai_client: OpenAI,
) -> str:
    """Generate a personalised VIP order confirmation email."""
    prompt = (
        f"Generate a warm VIP order confirmation for {customer_id} "
        f"ordering {product_name}. "
        "Mention priority handling, estimated delivery, and loyalty points."
    )
    return _llm_complete(prompt, max_tokens=200, temperature=0.4, openai_client=openai_client)


# ---------------------------------------------------------------------------
# Routing helpers (deterministic — zero LLM tokens)
# ---------------------------------------------------------------------------

def _route_digital(
    order: OrderInput,
    cache: FileCache,
    state_key: str,
) -> OrderResult:
    """Route a digital fulfilment order."""
    dispatch = f"dispatch:digital:immediate:{order.product_id}"
    cache.set(state_key, OrderStatus.PROCESSING_DIGITAL.value)
    logger.info("Order %s routed: digital immediate", order.order_id)
    return OrderResult(
        order_id=order.order_id,
        status=OrderStatus.PROCESSING_DIGITAL,
        dispatch_record=dispatch,
    )


def _route_physical(
    order: OrderInput,
    product: ProductData,
    cache: FileCache,
    state_key: str,
) -> OrderResult:
    """Route a physical fulfilment order, checking inventory."""
    if product.inventory > 0:
        dispatch = f"dispatch:physical:warehouse:{order.product_id}"
        cache.set(state_key, OrderStatus.PROCESSING_PHYSICAL.value)
        logger.info("Order %s routed: physical warehouse", order.order_id)
        return OrderResult(
            order_id=order.order_id,
            status=OrderStatus.PROCESSING_PHYSICAL,
            dispatch_record=dispatch,
        )
    else:
        dispatch = f"backorder:physical:notify:{order.customer_id}"
        cache.set(state_key, OrderStatus.BACKORDER_PHYSICAL.value)
        logger.info("Order %s routed: physical backorder", order.order_id)
        return OrderResult(
            order_id=order.order_id,
            status=OrderStatus.BACKORDER_PHYSICAL,
            dispatch_record=dispatch,
        )


def _route_subscription(
    order: OrderInput,
    customer: CustomerData,
    cache: FileCache,
    state_key: str,
) -> OrderResult:
    """Route a subscription renewal order."""
    sub_id = customer.subscription_id or order.customer_id
    dispatch = f"dispatch:subscription:renew:{sub_id}"
    cache.set(state_key, OrderStatus.PROCESSING_SUBSCRIPTION.value)
    logger.info("Order %s routed: subscription renew", order.order_id)
    return OrderResult(
        order_id=order.order_id,
        status=OrderStatus.PROCESSING_SUBSCRIPTION,
        dispatch_record=dispatch,
    )


def _route_vip(
    order: OrderInput,
    customer: CustomerData,
    product: ProductData,
    cache: FileCache,
    memory: MemoryLog,
    state_key: str,
    openai_client: OpenAI,
) -> OrderResult:
    """Route a VIP / high-value order with LLM confirmation email."""
    if customer.tier == "enterprise":
        email = generate_enterprise_confirmation(
            order.customer_id, product.name, openai_client
        )
        status = OrderStatus.PROCESSING_ENTERPRISE_VIP
        cache.set(state_key, status.value)
        memory.append("orders", "vip_enterprise", order.order_id, email)
        logger.info("Order %s routed: enterprise VIP with email", order.order_id)
    else:
        email = generate_vip_confirmation(
            order.customer_id, product.name, openai_client
        )
        status = OrderStatus.PROCESSING_VIP
        cache.set(state_key, status.value)
        memory.append("orders", "vip", order.order_id, email)
        logger.info("Order %s routed: standard VIP with email", order.order_id)

    return OrderResult(
        order_id=order.order_id,
        status=status,
        dispatch_record=f"dispatch:vip:{order.fulfilment_type.value}:{order.product_id}",
        confirmation_email=email,
    )


# ---------------------------------------------------------------------------
# VIP threshold (mirrors IR branch: order_value > 500)
# ---------------------------------------------------------------------------

VIP_THRESHOLD = 500.0


def _is_vip(order_value: float, fulfilment_type: FulfilmentType) -> bool:
    if fulfilment_type == FulfilmentType.DIGITAL and order_value > VIP_THRESHOLD:
        return True
    if fulfilment_type == FulfilmentType.PHYSICAL and order_value > VIP_THRESHOLD:
        return True
    if fulfilment_type == FulfilmentType.SUBSCRIPTION and order_value > 200.0:
        return True
    return False


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------

def process_order(
    order_id: str,
    customer_id: str,
    product_id: str,
    order_value: float,
    fulfilment_type: str,
    customer_api: str,
    product_api: str,
    cache: Optional[FileCache] = None,
    memory: Optional[MemoryLog] = None,
    openai_client: Optional[OpenAI] = None,
) -> OrderResult:
    """
    Process an inbound order event end-to-end.

    1. Deduplication check via cache.
    2. Input validation.
    3. Customer enrichment (HTTP).
    4. Fraud score check (threshold branch).
    5. Product enrichment + inventory check (HTTP).
    6. Fulfilment routing (digital / physical / subscription).
    7. VIP escalation (optional LLM confirmation email).
    """
    t0 = time.perf_counter()

    if cache is None:
        cache = FileCache()
    if memory is None:
        memory = MemoryLog()
    if openai_client is None:
        openai_client = OpenAI()

    state_key = f"order_state:{order_id}"

    # Step 1: deduplication
    cached_state = cache.get(state_key)
    if cached_state:
        logger.info("Order %s already processed: %s", order_id, cached_state)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.DUPLICATE,
            dispatch_record=f"duplicate:{order_id}",
            from_cache=True,
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    # Step 2: input validation
    if order_value <= 0:
        logger.warning("Order %s rejected: invalid order_value=%s", order_id, order_value)
        cache.set(state_key, OrderStatus.REJECTED_INVALID.value)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.REJECTED_INVALID,
            dispatch_record="rejected:invalid_order_value",
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    try:
        fulfilment = FulfilmentType(fulfilment_type)
    except ValueError:
        logger.warning("Order %s: unknown fulfilment_type=%s", order_id, fulfilment_type)
        cache.set(state_key, OrderStatus.REJECTED_INVALID.value)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.REJECTED_INVALID,
            dispatch_record=f"rejected:unknown_fulfilment_type:{fulfilment_type}",
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    order = OrderInput(
        order_id=order_id,
        customer_id=customer_id,
        product_id=product_id,
        order_value=order_value,
        fulfilment_type=fulfilment,
        customer_api=customer_api,
        product_api=product_api,
    )

    # Step 3: customer enrichment + fraud check
    try:
        customer = fetch_customer(customer_api, customer_id)
    except Exception as exc:
        logger.error("Customer enrichment failed for %s: %s", customer_id, exc)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.REJECTED_INVALID,
            dispatch_record="rejected:customer_enrichment_error",
            error=str(exc),
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    # Step 4: fraud gate
    if customer.fraud_score > 75:
        logger.warning(
            "Order %s blocked: fraud_score=%d", order_id, customer.fraud_score
        )
        cache.set(state_key, OrderStatus.REJECTED_FRAUD.value)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.REJECTED_FRAUD,
            dispatch_record=f"blocked:fraud_score={customer.fraud_score}",
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    # Step 5: product enrichment
    try:
        product = fetch_product(product_api, product_id)
    except Exception as exc:
        logger.error("Product enrichment failed for %s: %s", product_id, exc)
        return OrderResult(
            order_id=order_id,
            status=OrderStatus.REJECTED_INVALID,
            dispatch_record="rejected:product_enrichment_error",
            error=str(exc),
            processing_time_ms=(time.perf_counter() - t0) * 1000.0,
        )

    # Steps 6–8: routing
    if _is_vip(order.order_value, order.fulfilment_type):
        result = _route_vip(order, customer, product, cache, memory, state_key, openai_client)
    elif fulfilment == FulfilmentType.DIGITAL:
        result = _route_digital(order, cache, state_key)
    elif fulfilment == FulfilmentType.PHYSICAL:
        result = _route_physical(order, product, cache, state_key)
    else:
        result = _route_subscription(order, customer, cache, state_key)

    result.processing_time_ms = (time.perf_counter() - t0) * 1000.0
    return result


